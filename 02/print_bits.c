#include <unistd.h>

void	print_bits(char octet)
{
	int i;
	int n;
	char res;

	n = 128;
	i = 0;
	while (i++ < 8)
	{
		res = ((octet & n) > 0) ? '1' : '0';
		write(1, &res, 1);
		n >>= 1;
	}
}
