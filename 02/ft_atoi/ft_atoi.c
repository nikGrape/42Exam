int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int res;

	i = 0;
	while ((str[i] > 8 && str[i] < 20) || str[i] == ' ')
		i++;
	sign = (str[i] == '-') ? -1 : 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	res = 0;
	while(str[i] >= '0' && str[i] <= '9')
		res = res * 10 + (str[i++] - '0');
	return (res * sign);
}

